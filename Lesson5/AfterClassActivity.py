import math
print("Welcome to Trignometry calculator")
angle = int(input("Enter the angle of the triangle:"))
angler = math.radians(angle)
sin = math.sin(angler)
cos = math.cos(angler)
tan = math.tan(angler)
print("After calculating the sin, cos and tan, these are the results:\n")
print("Sin =", sin)
print("Cos =", cos)
print("Tan =", tan)

